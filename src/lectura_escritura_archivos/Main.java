    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
   // agregar filtro a filechooser
        FileNameExtensionFilter filter =
            new FileNameExtensionFilter ("Archivos .txt","txt","texto");
    fc.setFileFilter(filter);
    fc.showOpenDialog(this);
    int opcion = fc.showOpenDialog(this);
        if (opcion ==JFileChooser.APPROVE_OPTION) {
             archivo = fc.getSelectedFile();
             tf.setText(archivo.getAbsolutePath());
             
             
             //tomar el modelo de la tabla
             DefaultTableModel model = (DefaultTableModel) DateTable.getModel();
             
             
             
             try (Scanner lector = new Scanner(archivo)){
                //mientras archivo tenga otra linea
                while(lector.hasNextLine()){
                    //pedir la linea
                String linea = lector.nextLine();
                //separar los datos
                String[] datos = linea.split(",");
                long codigo = Long.parseLong(datos[0]);
                String nombre = datos[1];
                double promedio = Double.parseDouble(datos[2]);
                //agegar datos ala tabla
                model.addRow(new Object[]{codigo,nombre,promedio});
                }
            } catch (FileNotFoundException ex) {
                mostrarerror("error en el archivo","error al leer el arcivo");
            }catch (NumberFormatException ex) {
               
            }catch (Exception ex) {
                //todo eviar mensaje al usuario
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

