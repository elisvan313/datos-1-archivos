    void guardaraArchivo(){
        try (BufferedWriter bw= new BufferedWriter (new FileWriter(archivo))) {
            int Filas =DateTable.getRowCount();
            
            DefaultTableModel model = (DefaultTableModel) DateTable.getModel();
            for (int i = 0; i < Filas; i++) {
               Object codigo = model.getValueAt(i, 0);
               Object nombre = model.getValueAt(i, 1);
               Object promedio = model.getValueAt(i, 2);
               bw.write(codigo+","+nombre+","+promedio);
               bw.newLine();
            }
        } catch (IOException ex) {
            mostrarerror("Error Con Archivo","No se puede escribir en el archivo");
        } catch(NullPointerException ex){
        mostrarerror("Datos Faltantes","Los datos en la tabla no estan completos");
        limpiartabla(DateTable);
       }catch(NumberFormatException ex){
        mostrarerror("Error en datos","Verifique los datos ingresados");
       
       }catch(Exception ex){
        mostrarerror("Error","Error inesperado");
        ex.printStackTrace();
       }
    
    }
